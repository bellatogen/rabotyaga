async function main() {
  console.log('📥 Читаем taps:v1 из PostgreSQL…');
  const raw = await adapter.kvGet('taps:v1');
  if (!raw) {
    console.error('❌ taps:v1 не найден в PG. Запусти сервер хотя бы раз (ensureTapModel засеет).');
    process.exit(1);
  }

  const taps = JSON.parse(raw);
  console.log(`   Кранов в PG: ${taps.length}`);

  let patched = 0;
  for (const tap of taps) {
    if (MAPPING[tap.id]) {
      tap.iikoProductId = MAPPING[tap.id];
      patched++;
      console.log(`   ✅ ${tap.id} (${tap.name}): ${MAPPING[tap.id].length} имён`);
    } else {
      console.log(`   ⬜ ${tap.id} (${tap.name}): маппинг не задан`);
    }
  }

  await adapter.kvSet('taps:v1', JSON.stringify(taps));
  console.log(`\n✅ Записано в PG: ${patched} кранов с маппингом.`);
  console.log('\n⚠️  Теперь рестарт контейнера, чтобы сервер перечитал PG:');
  console.log('   docker restart rabotyaga-bot');
  console.log('\nПосле рестарта — дёрнуть refresh продаж (авторизованный запрос):');
  console.log('   curl -s -X POST https://rabotyaga55.ru/api/taps/refresh-sales -H "Cookie: rab_auth=<токен>"');

  // Завершаем соединение с PG
  const pool = require('../db/pool');
  await pool.end();
}

main().catch((e) => {
  console.error('❌', e.message);
  process.exit(1);
});
